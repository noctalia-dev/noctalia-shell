// Returns true for codepoints that are always displayed as emoji by default
// (Unicode Emoji_Presentation property). Digits 0-9 are NOT included.
inline bool isEmojiPresentation(char32_t cp) {
  const auto* begin = kEmojiPresentationRanges;
  const auto* end   = begin + std::size(kEmojiPresentationRanges);
  const auto* it    = std::upper_bound(begin, end, static_cast<std::uint32_t>(cp),
                                    [](std::uint32_t v, const Range& r) { return v < r.lo; });
  return it != begin && (--it)->hi >= static_cast<std::uint32_t>(cp);
}

// Codepoints that continue an in-progress emoji run but don't start one.
//   U+FE0F  — variation selector 16 (force emoji presentation)
//   U+200D  — zero-width joiner (links emoji in ZWJ sequences)
//   U+20E3  — combining enclosing keycap
//   U+E0020-U+E007F — tag characters (used in flag/subdivision sequences)
inline bool isEmojiContinuer(char32_t cp) {
  return cp == 0xFE0F || cp == 0x200D || cp == 0x20E3 ||
         (cp >= 0xE0020 && cp <= 0xE007F);
}

